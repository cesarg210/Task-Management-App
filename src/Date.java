public class Date {
    int yy;
    int mm;
    int dd;

    public Date(int yy, int mm, int dd) {
        this.yy = yy;
        this.mm = mm;
        this.dd = dd;
    }

    public int getYy() {
        return yy;
    }

    public void setYy(int yy) {
        this.yy = yy;
    }

    public int getMm() {
        return mm;
    }

    public void setMm(int mm) {
        this.mm = mm;
    }

    public int getDd() {
        return dd;
    }

    public void setDd(int dd) {
        this.dd = dd;
    }
}
